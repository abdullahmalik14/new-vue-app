/**
 * Section CSS Compiler
 * 
 * Compiles Tailwind CSS for each section using PostCSS and Tailwind CLI.
 * Generates minimal CSS files containing only the utilities used in each section.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';
import { dirname } from 'path';
import postcss from 'postcss';
import tailwindcss from 'tailwindcss';
import autoprefixer from 'autoprefixer';
import { extractClassesFromContentPaths, extractNumericUtilities, generateSafelistForNumericUtilities } from './classExtractor.js';
import { scanRouteConfigForSections, generateContentPathsForSection } from './sectionScanner.js';
import { filterContentPathsWithIgnores } from './ignoredComponentHandler.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

/**
 * Log helper for build-time logging
 */
function log(file, method, flag, description, data) {
  if (process.env.VITE_ENABLE_LOGGER === 'true') {
    const timestamp = new Date().toISOString();
    const dataStr = data ? JSON.stringify(data) : '{}';
    // eslint-disable-next-line no-console
    console.log(`[${timestamp}] [${file}] [${method}] [${flag}] ${description} ${dataStr}`);
  }
}

/**
 * Load base Tailwind config
 * Reads the main tailwind.config.js and extracts theme configuration
 * 
 * @returns {object} Base Tailwind config
 */
async function loadBaseTailwindConfig() {
  log('sectionCssCompiler.js', 'loadBaseTailwindConfig', 'start', 'Loading base Tailwind config', {});

  try {
    const configPath = path.join(__dirname, '../../tailwind.config.js');
    const configModule = await import(`file://${configPath}`);
    const baseConfig = configModule.default;

    log('sectionCssCompiler.js', 'loadBaseTailwindConfig', 'success', 'Base config loaded', {});

    return baseConfig;
  } catch (error) {
    log('sectionCssCompiler.js', 'loadBaseTailwindConfig', 'error', 'Error loading base config', {
      error: error.message
    });
    // Return minimal config
    return {
      theme: { extend: {} },
      plugins: []
    };
  }
}

/**
 * Generate Tailwind config for a specific section
 * Creates a config that only scans files in that section
 * 
 * @param {string} sectionName - Section name
 * @param {Array<string>} contentPaths - Array of content paths for this section
 * @param {object} baseConfig - Base Tailwind config
 * @param {Set<string>} extractedClasses - Optional pre-extracted classes
 * @returns {object} Section-specific Tailwind config
 */
export function generateSectionTailwindConfig(sectionName, contentPaths, baseConfig, extractedClasses = null) {
  log('sectionCssCompiler.js', 'generateSectionTailwindConfig', 'start', 'Generating section Tailwind config', {
    sectionName,
    contentPathCount: contentPaths.length
  });

  try {
    // Create section-specific config
    // Let Tailwind's JIT engine handle class extraction from content paths
    const sectionConfig = {
      content: contentPaths,
      darkMode: baseConfig.darkMode || 'class',
      theme: baseConfig.theme || { extend: {} },
      safelist: baseConfig.safelist || [],
      plugins: baseConfig.plugins || []
    };

    log('sectionCssCompiler.js', 'generateSectionTailwindConfig', 'success', 'Section config generated', {
      sectionName,
      contentPathCount: contentPaths.length
    });

    return sectionConfig;
  } catch (error) {
    log('sectionCssCompiler.js', 'generateSectionTailwindConfig', 'error', 'Error generating section config', {
      sectionName,
      error: error.message
    });
    throw error;
  }
}

/**
 * Compile CSS for a specific section
 * Uses PostCSS + Tailwind to generate the CSS file
 * 
 * @param {string} sectionName - Section name
 * @param {object} sectionConfig - Tailwind config for this section
 * @param {string} outputPath - Where to write the CSS file
 * @returns {Promise<string>} Path to generated CSS file
 */
export async function compileSectionCss(sectionName, sectionConfig, outputPath) {
  log('sectionCssCompiler.js', 'compileSectionCss', 'start', 'Compiling CSS for section', {
    sectionName,
    outputPath
  });

  try {
    // Create base CSS input
    const inputCss = `
/**
 * Section CSS: ${sectionName}
 * Generated: ${new Date().toISOString()}
 * This file is automatically generated by the Tailwind sectioned build system.
 */

@tailwind base;
@tailwind components;
@tailwind utilities;
`;

    // Process with PostCSS
    const result = await postcss([
      tailwindcss(sectionConfig),
      autoprefixer
    ]).process(inputCss, {
      from: undefined,
      to: outputPath
    });

    // Ensure output directory exists
    const outputDir = path.dirname(outputPath);
    if (!fs.existsSync(outputDir)) {
      fs.mkdirSync(outputDir, { recursive: true });
    }

    // Write CSS file
    fs.writeFileSync(outputPath, result.css, 'utf-8');

    log('sectionCssCompiler.js', 'compileSectionCss', 'success', 'CSS compiled', {
      sectionName,
      outputPath,
      size: result.css.length
    });

    return outputPath;
  } catch (error) {
    log('sectionCssCompiler.js', 'compileSectionCss', 'error', 'Error compiling CSS', {
      sectionName,
      error: error.message,
      stack: error.stack
    });
    throw error;
  }
}

/**
 * Compile CSS for all sections
 * Generates a CSS file for each section defined in routeConfig
 * 
 * @param {string} routeConfigPath - Path to routeConfig.json
 * @param {string} outputDir - Directory to write CSS files
 * @param {string} baseDir - Base directory for resolving paths
 * @returns {Promise<Map<string, string>>} Map of section name to CSS file path
 */
export async function compileAllSectionsCss(routeConfigPath, outputDir = 'dist/assets', baseDir = 'src') {
  log('sectionCssCompiler.js', 'compileAllSectionsCss', 'start', 'Compiling CSS for all sections', {
    outputDir
  });

  try {
    // Load base Tailwind config
    const baseConfig = await loadBaseTailwindConfig();

    // Scan sections from routeConfig
    const sections = scanRouteConfigForSections(routeConfigPath);

    // Results map
    const compiledFiles = new Map();

    // Compile each section
    for (const [sectionName, sectionMetadata] of sections.entries()) {
      try {
        console.log(`\n🔨 Building section: ${sectionName}`);

        // Generate content paths
        let contentPaths = generateContentPathsForSection(sectionName, sectionMetadata, baseDir);
        console.log(`  📁 Content paths: ${contentPaths.length}`);

        // Filter out ignored components
        contentPaths = filterContentPathsWithIgnores(contentPaths, baseDir);
        console.log(`  ✂️  After filtering: ${contentPaths.length} paths`);

        // Generate section config
        const sectionConfig = generateSectionTailwindConfig(sectionName, contentPaths, baseConfig);

        // Compile CSS
        const outputPath = path.join(outputDir, `section-${sectionName}.css`);
        const cssPath = await compileSectionCss(sectionName, sectionConfig, outputPath);

        compiledFiles.set(sectionName, cssPath);

        const stats = fs.statSync(cssPath);
        const sizeKB = (stats.size / 1024).toFixed(2);
        console.log(`✅ Compiled CSS for section: ${sectionName} -> ${sizeKB} KB`);
      } catch (error) {
        console.error(`❌ Failed to compile CSS for section: ${sectionName}`);
        console.error(`   Error: ${error.message}`);
        console.error(`   Stack: ${error.stack}`);
        log('sectionCssCompiler.js', 'compileAllSectionsCss', 'error', 'Section compilation failed', {
          sectionName,
          error: error.message,
          stack: error.stack
        });
      }
    }

    log('sectionCssCompiler.js', 'compileAllSectionsCss', 'success', 'All sections compiled', {
      sectionCount: compiledFiles.size
    });

    return compiledFiles;
  } catch (error) {
    log('sectionCssCompiler.js', 'compileAllSectionsCss', 'error', 'Error compiling all sections', {
      error: error.message,
      stack: error.stack
    });
    throw error;
  }
}

/**
 * Generate section CSS manifest
 * Creates a JSON manifest mapping section names to CSS files
 * 
 * @param {Map<string, string>} compiledFiles - Map of section to CSS path
 * @param {string} outputPath - Where to write the manifest
 * @returns {object} Manifest object
 */
export function generateSectionCssManifest(compiledFiles, outputPath) {
  log('sectionCssCompiler.js', 'generateSectionCssManifest', 'start', 'Generating CSS manifest', {
    sectionCount: compiledFiles.size
  });

  try {
    const manifest = {
      generated: new Date().toISOString(),
      version: '1.0.0',
      sections: {}
    };

    for (const [sectionName, cssPath] of compiledFiles.entries()) {
      const relativePath = path.relative(path.dirname(outputPath), cssPath).replace(/\\/g, '/');
      const stats = fs.statSync(cssPath);

      manifest.sections[sectionName] = {
        css: relativePath,
        size: stats.size,
        generated: stats.mtime.toISOString()
      };
    }

    // Ensure manifest directory exists
    const manifestDir = path.dirname(outputPath);
    if (!fs.existsSync(manifestDir)) {
      fs.mkdirSync(manifestDir, { recursive: true });
    }

    // Write manifest
    fs.writeFileSync(outputPath, JSON.stringify(manifest, null, 2), 'utf-8');

    log('sectionCssCompiler.js', 'generateSectionCssManifest', 'success', 'Manifest generated', {
      outputPath,
      sectionCount: compiledFiles.size
    });

    return manifest;
  } catch (error) {
    log('sectionCssCompiler.js', 'generateSectionCssManifest', 'error', 'Error generating manifest', {
      error: error.message
    });
    throw error;
  }
}

/**
 * Build all section CSS (main entry point)
 * Complete pipeline: scan -> extract -> compile -> manifest
 * 
 * @param {object} options - Build options
 * @returns {Promise<object>} Build result
 */
export async function buildAllSectionCss(options = {}) {
  const {
    routeConfigPath = path.join(__dirname, '../../src/router/routeConfig.json'),
    outputDir = path.join(__dirname, '../../dist/assets'),
    baseDir = path.join(__dirname, '../../src'),
    manifestPath = path.join(__dirname, '../../dist/section-css-manifest.json'),
    writeManifest = true
  } = options;

  console.log('\n🎨 Tailwind Section CSS Builder\n');
  console.log('Building CSS for each section...\n');

  try {
    // Compile all sections
    const compiledFiles = await compileAllSectionsCss(routeConfigPath, outputDir, baseDir);

    let manifest = null;
    if (writeManifest && manifestPath) {
      manifest = generateSectionCssManifest(compiledFiles, manifestPath);
    }

    console.log('\n✅ Section CSS build complete!');
    console.log(`\nGenerated ${compiledFiles.size} CSS files:`);
    for (const [sectionName, cssPath] of compiledFiles.entries()) {
      const size = fs.statSync(cssPath).size;
      const sizeKB = (size / 1024).toFixed(2);
      console.log(`  - ${sectionName}: ${sizeKB} KB`);
    }
    if (writeManifest && manifestPath) {
      console.log(`\n📋 Manifest: ${manifestPath}\n`);
    }

    return {
      success: true,
      filesGenerated: compiledFiles.size,
      manifest,
      files: Array.from(compiledFiles.entries()).map(([name, path]) => ({ name, path }))
    };
  } catch (error) {
    console.error('\n❌ Section CSS build failed:', error.message);
    console.error(error.stack);

    return {
      success: false,
      error: error.message
    };
  }
}

